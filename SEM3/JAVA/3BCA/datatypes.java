//program to display Data Types
import java.util.*;
class datatypes
{
	public static void main(String args[])
	{
		short a = 10;
		int b = 22512;
		long c = 123568769;
		float d = 22.7f;
		double e = 33.89;
		char f = 'M';
		String g = "Mitali Gupta";
		boolean h = true;
			System.out.println("\n\n LIST OF FEW BASIC DATA TYPES IN JAVA\n");
			System.out.println("\n Short value\t:\t"+a);
			System.out.println("\n Integer value\t:\t"+b);
			System.out.println("\n Long value\t:\t"+c);
			System.out.println("\n Float value\t:\t"+d);
			System.out.println("\n Double value\t:\t"+e);
			System.out.println("\n Character value:\t"+f);
			System.out.println("\n String value\t:\t"+g);
			System.out.println("\n Boolean value\t:\t"+h);
	}
}